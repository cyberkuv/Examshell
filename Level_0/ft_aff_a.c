#include <unistd.h>

int		main(int argc, char **argv)
{
	int i;

	i = 0;
	if (argc != 2)
	{
		write(1, "a", 1);
	}
	while (argc == 2 && argv[1][i] != '\0')
	{
		if (argv[1][i] == 'a')
		{
			write(1, &argv[1][i], 1);
		}
		i++;
	}
	write(1, "\n", 1);
}