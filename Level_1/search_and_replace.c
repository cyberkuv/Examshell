#include <unistd.h>

int		main(int argc, char **argv)
{
	int x = 0;
	if (argc == 4)
	{
		while (argv[1][x] != '\0')
		{
			if (argv[1][x] == argv[2][0])
				write(1, &argv[3][0], 1);
			else
				write(1, &argv[1][x], 1);
			x++;
		}
	}
	write(1, "\n", 1);
	return (0);
}