#include <unistd.h>

int     main(int ac, char **av)
{
    int s = 0, q = 0;

    if (ac == 2)
    {
        while (av[2][q])
        {
            if (av[1][s] == av[2][q])
                s++;
            q++;
        }
        if (av[1][s] != '\0')
        {
            s = 0;
            while (av[1][s])
            {
                write(1, &av[1][s], 1);
                s++;
            }
        }
    }
    write(1, "\n", 1);
    return (0);
}
