#include <unistd.h>

int     main(int ac, char **av)
{
    int x = 0;
    int y = 0;
    int pos = 0;

    if (ac == 3)
    {
        while (av[1][x] != '\0')
        {
            while (av[2][y] != '\0')
            {
                if (av[1][x] == av[2][y])
                {
                    pos++;
                    break ;
                }
                y++;
            }
            x++;
        }
        if (av[1][pos] == '\0')
            write(1, "1", 1);
        else
            write(1, "0", 1);
    }
    write(1, "\n", 1);
    return(0);
}
